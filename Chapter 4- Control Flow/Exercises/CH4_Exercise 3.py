# If block statement

alien_color = 'green'
if alien_color == 'green':
   print("Congratulations player , you've just earned 5 points")
elif alien_color == 'yellow':
   print("Congratulations player , you've just earned 10 points")
else: print ("Congratulations player , you've just earned 15 points")

