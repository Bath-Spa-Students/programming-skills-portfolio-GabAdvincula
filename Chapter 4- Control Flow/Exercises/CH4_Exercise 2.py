# If block statement

alien_color = 'green'
if alien_color == 'green':
   print("Congratulations , you've just earned 5 points for shooting the alien!!!")
else: 
   print("Congratulations , you've just earned 10 points!!!")

# Else block statement

alien_color = 'red'
if alien_color == 'green':
   print("Congratulations , you've just earned 5 points for shooting the alien!!!")
else: 
   print("Congratulations , you've just earned 10 points!!!")
