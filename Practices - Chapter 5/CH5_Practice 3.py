# Write a Python program to iterate through the values of a dictionary and print them.

personal_info = {
    'First_name': 'Gab',
    'Last_name': 'Advincula',
    'Age': '20',
    'Birthday': 'March 8, 2003',
    'Location': 'United Arab Emirates'
}

for value in personal_info.values():
    print(value)